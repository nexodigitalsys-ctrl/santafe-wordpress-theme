<li>No quieren hacer visita técnica antes de presupuestar</li>
<li>Piden más del 50% por adelantado</li>
<li>No tienen dirección fiscal ni CIF verificable</li>
<li>Presión para firmar "hoy porque la oferta expira"</li>
<li>No pueden mostrar obras en curso ni referencias recientes</li>
<li>El "jefe de obra" nunca visita la obra</li>
</ul>

