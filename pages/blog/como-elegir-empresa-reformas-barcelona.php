<p>En Barcelona hay cientos de empresas de reformas. Algunas excelentes, otras que desaparecen a mitad de obra. ¿Cómo distinguirlas antes de firmar? Después de 17 años en el sector, sabemos qué diferencia a las empresas serias de las que no lo son.</p>
<p>Estas 8 preguntas te ayudarán a tomar una decisión informada.</p>

<h2>Pregunta 1: ¿El presupuesto es cerrado y detallado?</h2>
<p>Un presupuesto vago es la principal señal de alerta. Si lees "reforma integral: 40.000 €" sin desglose de partidas, huye. Ese número puede convertirse en 60.000 € con los "extras".</p>
<p><strong>Qué debe incluir:</strong> Demolición por m², albañilería, electricidad completa, fontanería, carpintería interior, carpintería exterior, cocina (muebles+encimera), baños completos, pintura, limpieza, licencias.</p>

<h2>Pregunta 2: ¿Quién coordina la obra?</h2>
<p>En muchas empresas, el comercial vende, el jefe de obra supervisa desde la oficina, y los gremios trabajan sin dirección clara. Cuando surge un problema, nadie se responsabiliza.</p>
<p><strong>Qué buscar:</strong> Que una misma persona (el jefe de obra o el propietario) esté presente en la obra y sea tu interlocutor directo. En Santa Fe, Paulo coordina personalmente cada proyecto.</p>

<h2>Pregunta 3: ¿Tienen referencias verificables?</h2>
<p>Cualquiera puede mostrar fotos bonitas. Lo importante son referencias de clientes reales con obras similares a la tuya.</p>
<p><strong>Qué pedir:</strong> Contacto de 2-3 clientes de obras recientes. Visita a una obra en curso si es posible. Mira reseñas en Google Maps y no solo en su web.</p>

<h2>Pregunta 4: ¿Gestionan licencias y permisos?</h2>
<p>Una empresa que te dice "tú encárgate de la licencia" está externalizando una parte crítica del proyecto. Si la licencia se retrasa, la obra se retrasa.</p>
<p><strong>Qué buscar:</strong> Que incluyan la gestión de licencia en el alcance o al menos te asesoren sobre qué necesitas.</p>

<h2>Pregunta 5: ¿Cuál es el plazo realista?</h2>
<p>"Tu piso estará listo en 4 semanas" suena genial, pero una reforma integral de 80 m² nunca se hace en 4 semanas. Las promesas irreales son síntoma de inexperiencia o de que no te están contando toda la verdad.</p>
<p><strong>Qué esperar:</strong> 6-10 semanas para una reforma integral de 80 m² en condiciones normales. Si hay humedades, cambio de distribución o materiales especiales, más.</p>

<h2>Pregunta 6: ¿Cómo estructuran el pago?</h2>
<p>Evita empresas que pidan más del 40% antes de empezar. El pago debe estar vinculado a hitos de obra visibles y medibles.</p>
<p><strong>Estructura recomendada:</strong> 10% al firmar, 30% al inicio de obra, 30% a mitad, 30% a la entrega.</p>

<h2>Pregunta 7: ¿Ofrecen garantía por escrito?</h2>
<p>Una garantía verbal no vale nada. La empresa debe ofrecer garantía de ejecución y acabados por escrito. El mínimo aceptable es 1 año; 2 años es lo recomendable.</p>
<p><strong>Qué verificar:</strong> Que tengan seguro de responsabilidad civil activo. Que la garantía cubra tanto materiales como mano de obra.</p>

<h2>Pregunta 8: ¿Cómo resuelven imprevistos?</h2>
<p>En toda obra surgen imprevistos: una tubería oxidada, una viga oculta, una humedad no detectada. Lo importante no es que no haya problemas, sino cómo se resuelven.</p>
<p><strong>Qué buscar:</strong> Un protocolo claro: te informan antes de actuar, te presentan opciones con precios, y tu decides. Nada de "surprise invoices" al final.</p>

<h2>Señales de alerta (red flags)</h2>
