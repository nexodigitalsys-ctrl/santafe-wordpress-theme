<h2>El problema silencioso que destruye viviendas</h2>
<p>Las humedades son uno de los problemas más frecuentes en viviendas de Barcelona y Cataluña, especialmente en edificios construidos antes de los años noventa. Lo peligroso de la humedad es que suele ser invisible durante meses o incluso años, hasta que aparecen manchas en paredes, desconchados de pintura, olores a cerrado o, en casos graves, afecciones en la salud de los habitantes por la proliferación de moho.</p>
<p>En Santa Fe Construcciones intervenimos regularmente en viviendas con problemas de humedad. La buena noticia es que la mayoría de estos problemas tienen solución definitiva si se diagnostican correctamente. La mala noticia es que aplicar el tratamiento equivocado no solo no soluciona el problema, sino que a menudo lo enmascara, permitiendo que la estructura siga deteriorándose por dentro.</p>

<h2>Los tres tipos principales de humedad</h2>
<p>No todas las humedades son iguales. Cada tipo tiene una causa diferente y, por tanto, requiere un tratamiento específico. Confundirlos es el error más común entre propietarios e, incluso, algunos albañiles sin experiencia.</p>

<h3>1. Humedad por condensación</h3>
<p>Es la forma más habitual de humedad en viviendas, especialmente en Barcelona durante los meses de otoño e invierno. Se produce cuando el vapor de agua contenido en el aire interior de la casa (generado por respiración, cocinar, ducharse o secar ropa) encuentra una superficie fría (paredes exteriores, ventanas, esquinas) y se transforma en gotas líquidas.</p>
<p><strong>Cómo identificarla:</strong></p>
<ul>
<li>Aparece principalmente en paredes exteriores y esquinas del norte de la vivienda.</li>
<li>Es más intensa en invierno y desaparece o disminuye en verano.</li>
<li>Suele ir acompañada de moho negro o verde en juntas de azulejos, marcos de ventanas y siliconas.</li>
<li>Las ventanas empañan por dentro por las mañanas.</li>
</ul>
<p><strong>Causas raíz:</strong> Falta de ventilación, puente térmico en fachada, calefacción elevada sin renovación de aire, aislamiento deficiente y, en muchos casos, alta ocupación de la vivienda (muchos habitantes generan más vapor).</p>

<h3>2. Humedad por capilaridad</h3>
<p>Se produce cuando el agua del subsuelo asciende por los poros de los materiales de construcción (muros de piedra o ladrillo, morteros antiguos) debido a la acción capilar. Es extremadamente común en plantas bajas, sótanos habitables y edificios antiguos del centro de Barcelona, donde los cimientos carecen de una barrera de impermeabilización horizontal efectiva.</p>
<p><strong>Cómo identificarla:</strong></p>
<ul>
<li>Aparece en la parte baja de las paredes, desde el suelo hacia arriba, formando una marca característica en forma de arco.</li>
<li>La pintura se desconcha, el yeso se ablanda y puede caer en laminas.</li>
<li>El mortero o la piedra adquieren un tono más oscuro permanente.</li>
<li>Suele estar presente todo el año, sin variación estacional marcada.</li>
</ul>
<p><strong>Causas raíz:</strong> Ausencia o deterioro de la capa de impermeabilización horizontal en cimentación, rotura de tuberías enterradas, alto nivel freático o falta de drenaje perimetral.</p>

<h3>3. Humedad por filtración</h3>
<p>Esta humedad se debe a la entrada de agua desde el exterior a través de grietas, juntas deterioradas, rotura de bajantes, goteras en cubiertas o filtraciones a través de terrazas y balcones. Es la más agresiva porque el agua penetra con presión y puede afectar a la estructura del edificio.</p>
<p><strong>Cómo identificarla:</strong></p>
<ul>
<li>Aparece en puntos localizados, generalmente coincidiendo con fisuras, ventanas, balcones o techos bajo cubierta o terraza.</li>
<li>La mancha suele ser de forma irregular y puede extenderse rápidamente después de lluvias intensas.</li>
<li>Puede acompañarse de gotas o chorros de agua en episodios de lluvia.</li>
<li>El daño es más intenso cerca del punto de entrada.</li>
</ul>
<p><strong>Causas raíz:</strong> Grietas en fachada, rotura de membranas impermeabilizantes en terrazas, sellado deficiente de alfeizar, bajantes rotos, problemas en rejas de evacuación de cubierta o filtraciones desde viviendas superiores o adyacentes.</p>

<h2>Soluciones definitivas según el tipo de humedad</h2>

<h3>Tratamiento de la condensación</h3>
<p>Combate las causas, no solo el moho:</p>
<ul>
<li><strong>Ventilación forzada:</strong> Instalar sistemas de ventilación mecánica controlada (VMC) o, al menos, extractor en cocina y baños con funcionamiento por humedad.</li>
<li><strong>Aislamiento térmico:</strong> Colocar aislamiento en fachada interior (con cámara de aire y pladur) o en fachada exterior (SATE). Un muro bien aislado no enfría el aire interior hasta el punto de rocio.</li>
<li><strong>Calefacción uniforme:</strong> Evitar sobrecalentar una estancia y dejar otras frías. La diferencia de temperatura genera puntos de condensación.</li>
<li><strong>Pintura transpirable:</strong> Usar pinturas que permitan el paso del vapor de agua, evitando que quede atrapado en el paramento.</li>
</ul>
<p><strong>Coste orientativo:</strong> Una VMC sencilla cuesta entre 400 € y 800 € instalada. Aislar una fachada interior de un salón puede costar entre 60 € y 100 €/m².</p>

<h3>Tratamiento de la capilaridad</h3>
<p>Este es el problema que más tratamientos milagrosos inútiles acumula. No sirve pintar con pintura impermeable si el agua sigue subiendo por dentro del muro.</p>
<ul>
<li><strong>Corte de capilaridad por inyección:</strong> Se perforan agujeros a nivel de suelo en el muro y se inyectan productos silicatados o de resina que crean una barrera química impermeable. Es el método más efectivo en la mayoría de casos.</li>
<li><strong>Corte físico:</strong> Se realiza una ranura horizontal en el muro y se inserta una lámina impermeable. Es más invasivo pero definitivo en muros de piedra muy porosos.</li>
<li><strong>Drenaje perimetral:</strong> Si el problema es un alto nivel freático, puede ser necesario crear un sistema de drenaje alrededor del edificio para desviar el agua.</li>
<li><strong>Rehabilitación del paramento:</strong> Tras cortar la capilaridad, hay que eliminar el yeso deteriorado y aplicar morteros especiales transpirables y resistentes a sales.</li>
</ul>
<p><strong>Coste orientativo:</strong> Un corte por inyección en una vivienda de 80 m² puede costar entre 2.000 € y 4.000 €. El corte físico es más caro, entre 5.000 € y 8.000 € dependiendo del grosor y material de los muros.</p>

<h3>Tratamiento de filtraciones</h3>
<p>La clave está en encontrar exactamente por dónde entra el agua:</p>
<ul>
<li><strong>Impermeabilización de terrazas:</strong> Retirar el pavimento existente, aplicar una lámina impermeabilizante de PVC o EPDM, o realizar un tratamiento de poliuretano proyectado. Incluir pendientes correctas hacia los sumideros.</li>
<li><strong>Reparación de grietas:</strong> Sellado con morteros de reparación e inyección de resinas epoxi en fisuras estructurales.</li>
<li><strong>Sustitución de bajantes:</strong> Si la filtración viene de una bajante común rota en el interior del muro, es imprescindible cambiar el tramo afectado.</li>
<li><strong>Tratamiento de alfeizares y carpintería:</strong> Sustituir los sellados de silicona y aplicar productos hidrófugos en los revelados.</li>
</ul>
<p><strong>Coste orientativo:</strong> Impermeabilizar una terraza de 50 m² cuesta entre 2.500 € y 5.000 €. Reparar una grieta puede costar entre 500 € y 2.000 € según accesibilidad y gravedad.</p>

