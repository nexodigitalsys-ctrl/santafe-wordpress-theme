<p>Esta es una de las primeras preguntas que nos hacen los clientes. La respuesta corta es: <strong>depende del alcance de la reforma</strong>. No todas las obras requieren licencia, pero muchas sí, y comenzar sin la documentación adecuada puede costarte retrasos, multas e incluso tener que deshacer trabajos.</p>
<p>En Santa Fe gestionamos licencias de obra a diario en Barcelona, Girona y Tarragona. Esta guía resume todo lo que necesitas saber.</p>

<h2>¿Cuándo NO necesitas licencia?</h2>
<p>Las obras menores que no afectan a la estructura ni a elementos comunes del edificio normalmente no requieren licencia. Ejemplos:</p>
<ul>
<li>Pintar paredes y techos</li>
<li>Cambiar suelos sin modificar estructura</li>
<li>Sustituir sanitarios, grifería o muebles de cocina sin cambiar instalaciones</li>
<li>Colocar muebles o armarios empotrados</li>
<li>Instalar aire acondicionado split (con algunas excepciones en fachada)</li>
</ul>

<h2>¿Cuándo SÍ necesitas licencia de obra menor?</h2>
<p>Se requiere licencia de obra menor (también llamada obra menor o comunicación previa) cuando:</p>
<ul>
<li>Se modifican instalaciones de fontanería, electricidad o gas</li>
<li>Se derriban o construyen tabiques interiores (sin afectar estructura)</li>
<li>Se reforma completamente cocina o baño</li>
<li>Se sustituyen ventanas o puertas exteriores (sin modificar huecos)</li>
<li>Se instala suelo radiante</li>
<li>Se realizan trabajos de pladur con afectación a instalaciones</li>
</ul>

<h2>¿Cuándo necesitas licencia de obra mayor?</h2>
<p>La obra mayor requiere proyecto técnico firmado por arquitecto y aparejador:</p>
<ul>
<li>Modificación de elementos estructurales (vigas, pilares, forjados)</li>
<li>Apertura de huecos en fachada o muros de carga</li>
<li>Ampliación de superficie construida</li>
<li>Cambio de uso (local a vivienda, etc.)</li>
<li>Rehabilitación de fachada con modificación de aspecto</li>
<li>Construcción de elementos nuevos (terraza cubierta, etc.)</li>
</ul>

<h2>Documentación necesaria para obra menor</h2>
<p>En Barcelona, el ayuntamiento requiere:</p>
<ul>
<li>Formulario de solicitud cumplimentado</li>
<li>Fotocopia del DNI del propietario</li>
<li>Escritura de la propiedad o contrato de arrendamiento (con autorización del propietario)</li>
<li>Planos simples de la vivienda con la reforma marcada</li>
<li>Memoria descriptiva de las obras</li>
<li>Justificante de pago de tasas municipales</li>
</ul>

<h2>Documentación para obra mayor</h2>
<ul>
<li>Proyecto técnico firmado por arquitecto colegiado</li>
<li>Dirección facultativa (aparejador)</li>
<li>Estudio de seguridad y salud</li>
<li>Certificado energético (en algunos casos)</li>
<li>Memoria constructiva detallada</li>
<li>Planos de ejecución</li>
</ul>

<h2>Plazos de tramitación</h2>
<p>Los plazos varían según el ayuntamiento:</p>
<ul>
<li><strong>Obra menor en Barcelona:</strong> 15-30 días hábiles</li>
<li><strong>Obra mayor en Barcelona:</strong> 2-4 meses</li>
<li><strong>Girona y Tarragona:</strong> Generalmente más rápido, 10-20 días para obra menor</li>
</ul>

<h2>Errores comunes que retrasan la licencia</h2>
<h3>1. Presentar planos incompletos</h3>
<p>Los ayuntamientos devuelven solicitudes con planos que no muestran claramente el estado anterior y posterior. Los planos deben estar a escala y firmados.</p>

<h3>2. No incluir autorización del propietario</h3>
<p>Si eres inquilino, necesitas autorización escrita del propietario. Sin ella, el ayuntamiento no tramitará la licencia.</p>

<h3>3. Confundir obra menor con obra mayor</h3>
<p>Muchos solicitan obra menor cuando realmente necesitan obra mayor. Si el ayuntamiento detecta que el alcance es mayor, paralizarán la obra hasta que se regularice.</p>

<h3>4. Olvidar el permiso de ocupación de vía pública</h3>
<p>Si necesitas contenedor de escombros en la calle o montacargas, requieres permiso de ocupación de vía pública adicional.</p>

<h2>¿Cuánto cuesta la licencia?</h2>
<ul>
<li><strong>Obra menor Barcelona:</strong> 300 € - 800 € en tasas</li>
<li><strong>Obra mayor Barcelona:</strong> 1.500 € - 4.000 € en tasas (sin contar proyecto técnico)</li>
<li><strong>Proyecto técnico:</strong> 3.000 € - 8.000 € según complejidad</li>
</ul>

