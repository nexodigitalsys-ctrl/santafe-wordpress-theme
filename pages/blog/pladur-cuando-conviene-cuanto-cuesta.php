<h2>Tipos de placa y cuándo usar cada una</h2>
<h3>Placa estándar (blanca)</h3>
<p>Uso general en tabiques y techos de zonas secas. Espesores: 10 mm, 13 mm, 15 mm. La de 13 mm es la más común para tabiques.</p>

<h3>Placa antihumedad (verde)</h3>
<p>Indispensable en baños, cocinas y zonas con vapor. Resiste la humedad ambiental sin deformarse ni proliferar moho.</p>

<h3>Placa resistente al fuego (rosa/gris)</h3>
<p>Obligatoria en conductos de evacuación, garajes comunitarios y separaciones entre viviendas según CTE.</p>

<h3>Placa de alta dureza</h3>
<p>Para zonas de alto impacto: pasillos de colegios, hospitales, locales comerciales.</p>

<h2>Aislamiento: la clave del confort</h2>
<p>El pladur sin aislamiento es solo una división estética. Para obtener verdaderos beneficios:</p>
<ul>
<li><strong>Aislamiento acústico:</strong> Lana mineral de roca entre las placas reduce el ruido en 35-45 dB. Esencial en dormitorios y entre vecinos.</li>
<li><strong>Aislamiento térmico:</strong> Lana mineral o poliestireno extruido mejora la eficiencia energética, reduciendo facturas de calefacción y aire acondicionado.</li>
<li><strong>Barrera de vapor:</strong> En zonas húmedas, evita que el vapor penetre en el aislamiento y cause condensación.</li>
</ul>

<h2>¿Se pueden colgar muebles en pladur?</h2>
<p>Sí, pero con precauciones:</p>
<ul>
<li><strong>Peso ligero (cuadros, estanterías pequeñas):</strong> tacos especiales para pladur.</li>
<li><strong>Peso medio (muebles de cocina, estanterías):</strong> Refuerzo interior con tablero de madera en la estructura metálica durante la instalación.</li>
<li><strong>Peso pesado (muebles de baño suspendidos, televisores grandes):</strong> Anclaje directo a la pared base tras el pladur, no al propio pladur.</li>
</ul>
<p>En Santa Fe prevemos los refuerzos necesarios durante el proyecto para que puedas colgar todo lo que necesites.</p>

<h2>Ventajas del pladur frente a la albañilería tradicional</h2>
<ul>
<li><strong>Rapidez:</strong> Una habitación de 20 m² se puede pladurizar en 1-2 días.</li>
<li><strong>Limpieza:</strong> No genera escombros ni humedad de obra.</li>
<li><strong>Flexibilidad:</strong> Fácil de modificar en el futuro.</li>
<li><strong>Peso:</strong> Mucho más ligero que el ladrillo, ideal para forjados.</li>
<li><strong>Instalaciones:</strong> Fácil paso de electricidad, aire acondicionado y extracción.</li>
</ul>

<h2>Desventajas y limitaciones</h2>
<ul>
<li><strong>Resistencia al impacto:</strong> Inferior al ladrillo. Una patada fuerte puede agujerear la placa.</li>
<li><strong>Carga estructural:</strong> No sirve para muros de carga ni contención.</li>
<li><strong>Humedad directa:</strong> El agua estancada daña el yeso. Nunca en duchas sin protección.</li>
</ul>

<h2>Errores comunes al instalar pladur</h2>
<h3>1. No usar placa adecuada para cada zona</h3>
<p>Usar placa estándar en un baño es un error que causa moho en meses.</p>

<h3>2. Omitir el aislamiento</h3>
<p>Instalar pladur sin lana mineral es como poner una cortina fina contra el frío y el ruido.</p>

<h3>3. Junta mal tratada</h3>
<p>Las juntas entre placas deben tratarse con cinta de fibra de vidrio y masilla especial. Si no, aparecerán fisuras.</p>

<h3>4. Estructura mal anclada</h3>
<p>La estructura metálica debe anclarse firmemente al techo y suelo. Si no, el tabique vibrará y acabará agrietándose.</p>

